--[[
Client-side player-following blizzard
The BlizzardClientScript will has a custom property for the blizzard template to spawn and follow above the player.
The InteriorZones Folder holds Triggers that will make the blizzard effect invisible when the player is in them.
 You can add more triggers to your liking.
 You can mask the sudden invisibility with more static blizzard effects at entrances.
 Feel free to replace the blizzard template with alternate effects such as rain.
]]--